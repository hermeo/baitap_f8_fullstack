const maxPageCount = Math.ceil(wordArray.length / 2);
const findWordTiming = function (time) {
  var index = wordArray.findIndex(function (item) {
    if (time >= item[0].startTime && time <= item[item.length - 1].endTime) {
      return true;
    }
  });

  if (
    time < wordArray[0][0].startTime - 5000 ||
    time >
      wordArray[wordArray.length - 1][
        wordArray[wordArray.length - 1].length - 1
      ].endTime +
        5000
  ) {
    karaokeContent.innerText = "";
    karaokeContent.append(songInfoElement);
  }

  if (index !== -1) {
    if (
      wordArray[index + 1] &&
      wordArray[index][wordArray[index].length - 1].endTime -
        wordArray[index + 1][0].startTime <
        -10000
    ) {
      setTimeout(function () {
        karaokeContent.innerText = "";
        karaokeContent.append(songInfoElement);
      }, 5000);
    } else {
      const page = Math.floor(index / 2 + 1);
      const offset = (page - 1) * 2;
      const container = document.createElement("div");
      if (index >= offset && index < offset + 2) {
        for (let i = offset; i < offset + 2; i++) {
          const paragraph = document.createElement("p");

          wordArray[i].forEach(function (item, wordIndex) {
            const wordElement = document.createElement("span");
            wordElement.classList.add("word");
            wordElement.dataset.index = wordIndex;
            wordElement.innerText = item.data;
            const innerSpan = document.createElement("span");
            innerSpan.innerText = item.data;
            wordElement.append(innerSpan);

            paragraph.append(wordElement);
          });

          container.append(paragraph);
        }
      }

      karaokeContent.append(container);

      if (container.previousElementSibling) {
        container.previousElementSibling.remove();
      }
    }
  }
};

